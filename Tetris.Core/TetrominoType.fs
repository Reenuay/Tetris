/// <summary>
/// Contains the TetrominoType type and its associated functions.
/// </summary>
[<RequireQualifiedAccess>]
module Tetris.Core.TetrominoType


/// <summary>
/// Represents the tetrominoes that can be used in the game.
/// - I: Line
/// - J: Reverse L-shape
/// - L: L-shape
/// - O: Square
/// - S: S-shape
/// - T: T-shape
/// - Z: Z-shape
/// </summary>
type TetrominoType =
    | I
    | J
    | L
    | O
    | S
    | T
    | Z
